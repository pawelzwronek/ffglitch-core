static const AVClass sdl_class = {
    .class_name = "quickjs-sdl",
    .item_name  = av_default_item_name,
    .version    = LIBAVUTIL_VERSION_INT,
};

typedef struct SDL_Event_Element {
    struct SDL_Event_Element *next;
    SDL_Event event;
} SDL_Event_Element;

typedef struct {
    const AVClass *class;
    pthread_mutex_t sdl_mutex;
    SDL_Joystick *joystick;
    SDL_GameController *gamecontroller;
    SDL_Event_Element *first_sdl_event;
    SDL_Event_Element *last_sdl_event;
} SDLContext;

/*********************************************************************/
static int get_SDL_Event(SDLContext *ctx, SDL_Event *event)
{
    SDL_Event_Element *element;
    int ret = 0;

    pthread_mutex_lock(&ctx->sdl_mutex);

    element = ctx->first_sdl_event;
    if ( element != NULL )
    {
        memcpy(event, &element->event, sizeof(SDL_Event));
        ctx->first_sdl_event = element->next;
        av_freep(&element);
        ret = 1;
    }

    pthread_mutex_unlock(&ctx->sdl_mutex);

    return ret;
}

static void add_SDL_Event(SDLContext *ctx, const SDL_Event *event)
{
    SDL_Event_Element *element;

    pthread_mutex_lock(&ctx->sdl_mutex);

    element = (SDL_Event_Element *) av_malloc(sizeof(SDL_Event_Element));
    element->next = NULL;
    memcpy(&element->event, event, sizeof(SDL_Event));
    if ( ctx->first_sdl_event == NULL )
        ctx->first_sdl_event = element;
    else
        ctx->last_sdl_event->next = element;
    ctx->last_sdl_event = element;

    pthread_mutex_unlock(&ctx->sdl_mutex);
}

/*********************************************************************/
static pthread_mutex_t sdlctxs_lock = PTHREAD_MUTEX_INITIALIZER;
static SDLContext **sdlctxs;
static int       nb_sdlctxs;

int ff_quickjs_sdl_do_events(void)
{
    return (nb_sdlctxs > 0);
}

void ff_quickjs_sdl_event_add(const SDL_Event *event)
{
    pthread_mutex_lock(&sdlctxs_lock);
    for ( int i = 0; i < nb_sdlctxs; i++ )
        add_SDL_Event(sdlctxs[i], event);
    pthread_mutex_unlock(&sdlctxs_lock);
}

/*********************************************************************/
static JSClassID js_SDL_class_id;

static JSValue SDL_ctor(JSContext *ctx, JSValueConst new_target, int argc, JSValueConst *argv)
{
    JSValue obj = JS_NULL;
    if ( SDL_WasInit(SDL_INIT_EVERYTHING) != 0 )
    {
        SDLContext *sdlctx;
        int sdlctx_idx;

        /* using new_target to get the prototype is necessary when the
           class is extended. */
        JSValue proto = JS_GetPropertyStr(ctx, new_target, "prototype");
        if ( JS_IsException(proto) )
            return JS_EXCEPTION;
        obj = JS_NewObjectProtoClass(ctx, proto, js_SDL_class_id);
        JS_FreeValue(ctx, proto);
        if ( JS_IsException(obj) )
        {
            JS_FreeValue(ctx, obj);
            return JS_EXCEPTION;
        }

        sdlctx = (SDLContext *) av_mallocz(sizeof(SDLContext));
        sdlctx->class = &sdl_class;
        pthread_mutex_init(&sdlctx->sdl_mutex, NULL);
        JS_SetOpaque(obj, sdlctx);

        /* add to sdlctxs */
        pthread_mutex_lock(&sdlctxs_lock);
        sdlctx_idx = nb_sdlctxs;
        sdlctxs = av_realloc_array(sdlctxs, ++nb_sdlctxs, sizeof(*sdlctxs));
        sdlctxs[sdlctx_idx] = sdlctx;
        pthread_mutex_unlock(&sdlctxs_lock);
    }
    return obj;
}

static void js_SDL_finalizer(JSRuntime *rt, JSValue val)
{
    SDLContext *sdlctx = (SDLContext *) JS_GetOpaque(val, js_SDL_class_id);

    /* remove from sdlctxs */
    pthread_mutex_lock(&sdlctxs_lock);
    for ( int i = 0; i < nb_sdlctxs; i++ )
    {
        if ( sdlctxs[i] == sdlctx )
        {
            int ip1 = i + 1;
            if ( ip1 != nb_sdlctxs )
                memmove(&sdlctxs[i], &sdlctxs[ip1], (nb_sdlctxs-ip1) * sizeof(*sdlctxs));
            break;
        }
    }
    sdlctxs = av_realloc_array(sdlctxs, --nb_sdlctxs, sizeof(*sdlctxs));
    pthread_mutex_unlock(&sdlctxs_lock);

    /* destroy and free sdlctx */
    if ( sdlctx->joystick != NULL )
        SDL_JoystickClose(sdlctx->joystick);
    if ( sdlctx->gamecontroller != NULL )
        SDL_GameControllerClose(sdlctx->gamecontroller);
    pthread_mutex_destroy(&sdlctx->sdl_mutex);
    av_free(sdlctx);
}

/*********************************************************************/
static JSValue
js_SDL_NumJoysticks(JSContext *ctx, JSValueConst this_val, int argc, JSValueConst *argv)
{
    return JS_NewInt32(ctx, SDL_NumJoysticks());
}

static JSValue
js_SDL_JoystickOpen(JSContext *ctx, JSValueConst this_val, int argc, JSValueConst *argv)
{
    SDLContext *sdlctx = (SDLContext *) JS_GetOpaque(this_val, js_SDL_class_id);
    JSValue jnum = argv[0];
    int num;

    if ( !JS_IsInt32(jnum) )
    {
        JS_ThrowTypeError(ctx, "SDL_JoystickOpen() takes an integer as argument");
        return JS_EXCEPTION;
    }

    num = JS_VALUE_GET_INT(jnum);

    sdlctx->joystick = SDL_JoystickOpen(num);

    return JS_TRUE;
}

static JSValue
js_SDL_JoystickClose(JSContext *ctx, JSValueConst this_val, int argc, JSValueConst *argv)
{
    SDLContext *sdlctx = (SDLContext *) JS_GetOpaque(this_val, js_SDL_class_id);

    if ( sdlctx->joystick != NULL )
        SDL_JoystickClose(sdlctx->joystick);
    sdlctx->joystick = NULL;

    return JS_NULL;
}

