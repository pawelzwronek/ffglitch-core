static void init_SDL(JSContext *ctx, JSValueConst global_object)
{
    JSRuntime *rt = JS_GetRuntime(ctx);
    JSValue SDL_proto = JS_NewObject(ctx);
    JSValue jSDL_ctor = JS_NewCFunction2(ctx, SDL_ctor, "SDL", 3, JS_CFUNC_constructor, 0);
    JS_NewClassID(&js_SDL_class_id);
    JS_NewClass(rt, js_SDL_class_id, &js_SDL_class);
    JS_SetPropertyFunctionList(ctx, SDL_proto, js_SDL_proto_funcs, FF_ARRAY_ELEMS(js_SDL_proto_funcs));
    JS_SetConstructor(ctx, jSDL_ctor, SDL_proto);
    JS_SetPropertyFunctionList(ctx, jSDL_ctor, js_SDL_obj, FF_ARRAY_ELEMS(js_SDL_obj));
    JS_SetClassProto(ctx, js_SDL_class_id, SDL_proto);
    JS_SetPropertyStr(ctx, global_object, "SDL", jSDL_ctor);
}

/*********************************************************************/
void ff_quickjs_sdl_init(JSContext *ctx, JSValueConst global_object)
{
    init_SDL(ctx, global_object);
}
