static JSValue
js_SDL_GetEvent(JSContext *ctx, JSValueConst this_val, int argc, JSValueConst *argv)
{
    SDLContext *sdlctx = (SDLContext *) JS_GetOpaque(this_val, js_SDL_class_id);
    SDL_Event event;
    JSValue jevent = JS_NULL;

    if ( get_SDL_Event(sdlctx, &event) )
    {
        /* prepare args */
        jevent = JS_NewObject(ctx);
        JS_SetPropertyStr(ctx, jevent, "type", JS_NewInt32(ctx, event.type));
        JS_SetPropertyStr(ctx, jevent, "timestamp", JS_NewInt32(ctx, event.common.timestamp));
        switch ( event.type )
        {
        case SDL_KEYDOWN:
        case SDL_KEYUP:
            {
                JSValue jkeysym = JS_NewObject(ctx);
                JS_SetPropertyStr(ctx, jevent, "state", JS_NewInt32(ctx, event.key.state));
                JS_SetPropertyStr(ctx, jevent, "repeat", JS_NewInt32(ctx, event.key.repeat));
                JS_SetPropertyStr(ctx, jevent, "keysym", jkeysym);
                JS_SetPropertyStr(ctx, jkeysym, "scancode", JS_NewInt32(ctx, event.key.keysym.scancode));
                JS_SetPropertyStr(ctx, jkeysym, "sym", JS_NewInt32(ctx, event.key.keysym.sym));
                JS_SetPropertyStr(ctx, jkeysym, "mod", JS_NewInt32(ctx, event.key.keysym.mod));
            }
            break;
        case SDL_JOYAXISMOTION:
            JS_SetPropertyStr(ctx, jevent, "which", JS_NewInt32(ctx, event.jaxis.which));
            JS_SetPropertyStr(ctx, jevent, "axis", JS_NewInt32(ctx, event.jaxis.axis));
            JS_SetPropertyStr(ctx, jevent, "value", JS_NewInt32(ctx, event.jaxis.value));
            break;
        case SDL_JOYBALLMOTION:
            JS_SetPropertyStr(ctx, jevent, "which", JS_NewInt32(ctx, event.jball.which));
            JS_SetPropertyStr(ctx, jevent, "ball", JS_NewInt32(ctx, event.jball.ball));
            JS_SetPropertyStr(ctx, jevent, "xrel", JS_NewInt32(ctx, event.jball.xrel));
            JS_SetPropertyStr(ctx, jevent, "yrel", JS_NewInt32(ctx, event.jball.yrel));
            break;
        case SDL_JOYHATMOTION:
            JS_SetPropertyStr(ctx, jevent, "which", JS_NewInt32(ctx, event.jhat.which));
            JS_SetPropertyStr(ctx, jevent, "hat", JS_NewInt32(ctx, event.jhat.hat));
            JS_SetPropertyStr(ctx, jevent, "value", JS_NewInt32(ctx, event.jhat.value));
            break;
        case SDL_JOYBUTTONDOWN:
        case SDL_JOYBUTTONUP:
            JS_SetPropertyStr(ctx, jevent, "which", JS_NewInt32(ctx, event.jbutton.which));
            JS_SetPropertyStr(ctx, jevent, "button", JS_NewInt32(ctx, event.jbutton.button));
            JS_SetPropertyStr(ctx, jevent, "state", JS_NewInt32(ctx, event.jbutton.state));
            break;
        case SDL_CONTROLLERAXISMOTION:
            JS_SetPropertyStr(ctx, jevent, "which", JS_NewInt32(ctx, event.caxis.which));
            JS_SetPropertyStr(ctx, jevent, "axis", JS_NewInt32(ctx, event.caxis.axis));
            JS_SetPropertyStr(ctx, jevent, "value", JS_NewInt32(ctx, event.caxis.value));
            break;
        case SDL_CONTROLLERBUTTONDOWN:
        case SDL_CONTROLLERBUTTONUP:
            JS_SetPropertyStr(ctx, jevent, "which", JS_NewInt32(ctx, event.cbutton.which));
            JS_SetPropertyStr(ctx, jevent, "button", JS_NewInt32(ctx, event.cbutton.button));
            JS_SetPropertyStr(ctx, jevent, "state", JS_NewInt32(ctx, event.cbutton.state));
            break;
        }
    }

    return jevent;
}

